package com.dayOne.practice;

public class PrmitiveDataType {

	static short value1;
	static byte value2;
	static int value3;
	static long value4;
	static double value5;
	static float value6;
	static char value7;
	static boolean value8;
	static String value9;

	public static void main(String[] args) {
		System.out.println("Default values of all Prmitive Data Type:");
		System.out.println("Integral Data Type:");
		System.out.println("short = " + value1);
		System.out.println("byte = " + value2);
		System.out.println("int = " + value3);
		System.out.println("long = " + value4);
		System.out.println("Floating Point Data Type:");
		System.out.println("double = " + value5);
		System.out.println("float = " + value6);
		System.out.println("Non Numaric Data Type:");
		System.out.println("char = " + value7);
		System.out.println("boolean = " + value8);
		System.out.println("String = " + value9);
	}

}
